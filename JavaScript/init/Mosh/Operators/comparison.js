let x = 1;

//relational
console.log(x > 1);
console.log(x > 0);
console.log(x >= 1);

console.log(x < 1);
console.log(x <= 1);

//Equality
console.log(x === 1);
console.log(x !== 1);

//Strict Equality
console.log(1 === 1);
console.log('1' === 1);
console.log(true === 1);

//Loose Equality
console.log(1 == 1);
console.log(1 == '1');
console.log(true == 1);






